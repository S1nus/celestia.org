class GlossaryContent extends React.Component {
    render() {
        return (
            <div className={'glossary-content'}>

                <p>Off-chain data availability occurs when an L2 publishes its transaction data somewhere separate from the L1 it settles on. For example, if an L2s settles to Ethereum but publishes its data to Celestia, it is using Celestia for off-chain data availability. If that L2 is settling and publishing its data to Ethereum, it is using on-chain data availability. L2’s that use on-chain data availability are called rollups while L2’s that use off-chain data availability are called validiums or plasma.
                </p>

            </div>
        )
    }
}

